namespace KitchenSink
{
    /// <summary>
    /// Result of comparing two values.
    /// </summary>
    public enum Comparison
    {
        /// <summary>
        /// Greater than.
        /// </summary>
        GT = 1,
        /// <summary>
        /// Lesser than.
        /// </summary>
        LT = -1,
        /// <summary>
        /// Equal to.
        /// </summary>
        EQ = 0
    }
}
