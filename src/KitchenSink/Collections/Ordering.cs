namespace KitchenSink.Collections
{
    /// <summary>
    /// Result of comparing two values.
    /// </summary>
    public enum Ordering
    {
        /// <summary>
        /// Greater than.
        /// </summary>
        Gt = 1,
        /// <summary>
        /// Lesser than.
        /// </summary>
        Lt = -1,
        /// <summary>
        /// Equal to.
        /// </summary>
        Eq = 0
    }
}
